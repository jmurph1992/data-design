		<p>Objective: To filter search results to make finding the right product easier.</p>

		<h1>Interactive Flow</h1>

		<ol>
			<li>Henry enters a product type he is looking for in the search bar and clicks "search"</li>
			<li>A long list with many options is displayed</li>
			<li>Henry clicks the "filter search results" button</li>
			<li>A menu opens with multiple ways to filter results (brands, pricing, models, etc.)</li>
			<li>Henry makes his selections and clicks "update"</li>
			<li>A shorter list with the new results is displayed</li>
			<li>Henry can now browse the products that more closely match what he needs</li>
		</ol>

